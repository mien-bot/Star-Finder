function buildingsToSvg(buildings: Building[]): string {
  const viewBox = "0 0 100 100";
  
  let paths = buildings.map((building, idx) => {
    const points = building.points.map(p => `${p.x},${p.y}`).join(" ");
    return `
      <polygon 
        points="${points}" 
        fill="rgba(59, 130, 246, 0.3)" 
        stroke="#2563eb" 
        stroke-width="0.5"
      />
      <text x="${building.points[0].x}" y="${building.points[0].y - 2}" 
        font-size="3" fill="#1e40af">B${idx + 1}</text>
    `;
  }).join("\n");

  return `
    <svg xmlns="http://www.w3.org/2000/svg" viewBox="${viewBox}" 
      style="background-color: #fafafa; width: 100%; height: 100%;">
      <!-- Grid lines -->
      <defs>
        <pattern id="grid" width="10" height="10" patternUnits="userSpaceOnUse">
          <path d="M 10 0 L 0 0 0 10" fill="none" stroke="#e5e5e5" stroke-width="0.2"/>
        </pattern>
      </defs>
      <rect width="100" height="100" fill="url(#grid)" />
      
      <!-- Property boundary -->
      <rect x="5" y="5" width="90" height="90" 
        fill="none" stroke="#737373" stroke-width="0.5" stroke-dasharray="2,1"/>
      
      <!-- Buildings -->
      ${paths}
      
      <!-- Legend -->
      <text x="5" y="97" font-size="2.5" fill="#525252">Generated by HYLO-SP</text>
    </svg>
  `.trim();
}

export async function POST(request: NextRequest) {
  try {
    const body = await request.json();
    const { image, source } = body;

    if (!image) {
      return NextResponse.json(
        { error: "Image is required" },
        { status: 400 }
      );
    }

    // Validate image format (base64 or URL)
    const isValidFormat = 
      image.startsWith("data:image/") || 
      image.startsWith("http://") || 
      image.startsWith("https://");
    
    if (!isValidFormat) {
      return NextResponse.json(
        { error: "Invalid image format. Must be base64 or URL" },
        { status: 400 }
      );
    }

    // Detect buildings using Vision API
    // In production, pass the image to your Vision API here
    const buildings = await detectBuildings(image);
    
    // Generate SVG from detected buildings
    const svg = buildingsToSvg(buildings);

    return NextResponse.json({
      svg,
      buildings,
      message: "Buildings detected successfully",
    });
  } catch (error) {
    console.error("Error processing image:", error);
    return NextResponse.json(
      { error: "Failed to process image" },
      { status: 500 }
    );
  }
}
